                    <h2>Engaging and informing patients: Health information technology use in community health centers</h2>
				        
                        <p2>1. Introduction
Healthcare organizations foster patient engagement by ensuring care coordination and facilitating information exchange between clinicians and patients [1]. Information usage and processes are the archetypal examples of firm-based antecedents related to successful patient engagement [2]. Patient engagement is referred to as actions and efforts that target more active patient involvement in health and healthcare [3], [4], empowering patients to become the center of their care. Patient engagement entails a varying degree of patient and clinician interactions ranging from one-direction communication (e.g., a clinician to a patient) to multidirectional communication (e.g., between a community of clinicians and patients) [5]. Health information technology (HIT) is a critical medium in patient engagement and has proven effective in improving health outcomes, becoming a major strategic focus of healthcare organizations [3], [6], [7], [8].

Despite its increasing prevalence, there are pervasive disparities in the use of HIT across healthcare organizations and patient users [9], [10], [11]. While HIT has received growing attention from both practice and academia, little is known about disparities in relation to patient engagement HIT. Following the CMS (Centers for Medicare and Medicaid Services) EHR (electronic health record) Incentive Program, which targeted to facilitate modernizing health records across all healthcare organizations, [12] increasing numbers of healthcare organizations have formally adopted EHR and various HIT functions as part of their operations [13], [14], [15]. However, the diffusion of patient engagement HIT has not been uniform across healthcare organizations [16], posing a question as to whether the benefits of patient engagement HIT are equally distributed across organizations and consequently across patients. Therefore, examining disparities in patient engagement HIT and usage across healthcare organizations can lead to essential insights and suggestions for improved practice.

In this paper, patient engagement is examined from the perspective of organization HIT and extended to create a two-part dichotomy of patient engagement comprehensiveness: 1) engagement that is defined as activities where the organization is providing and the patient is using HIT to simultaneously exchange information as equal participants related to patient healthcare, and 2) informing that is defined as activities where the organization is providing patient healthcare information using HIT for the purpose of communications and information flow between the patient and the health care provider. The former is considered a more comprehensive form of patient engagement relative to the latter, requiring greater involvement of patients [5]. It should be noted that these concepts are not mutually exclusive as engagement also encompasses informing patients and families. Rather, they should be considered as conceptual foundations to examine the comprehensiveness and disparities in patient engagement HIT use. Note that and the terminology used in this paper reflects both the provider and the patient regarding HIT. In other words, we refer to using HIT as part of providing and receiving care.</p2>

